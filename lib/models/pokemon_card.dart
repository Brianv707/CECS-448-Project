class PokemonCard {
  final String id;
  final String name;
  final String number;
  final String imageUrl;
  final String rarity;
  final String setId;
  
  PokemonCard({
    required this.id,
    required this.name,
    required this.number,
    required this.imageUrl,
    required this.rarity,
    required this.setId,
  });
  
  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'name': name,
      'number': number,
      'imageUrl': imageUrl,
      'rarity': rarity,
      'setId': setId,
    };
  }
  
  factory PokemonCard.fromJson(Map<String, dynamic> json) {
    return PokemonCard(
      id: json['id'],
      name: json['name'],
      number: json['number'],
      imageUrl: json['imageUrl'],
      rarity: json['rarity'],
      setId: json['setId'],
    );
  }
  
